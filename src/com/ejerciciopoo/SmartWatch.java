package com.ejerciciopoo;

public class SmartWatch {
    int duracionbateria;
    boolean podometro;
    int capacidadDeMemoria;

    public SmartWatch() {
    }

    public SmartWatch(int duracionbateria, boolean podometro, int capacidadDeMemoria) {
        this.duracionbateria = duracionbateria;
        this.podometro = podometro;
        this.capacidadDeMemoria = capacidadDeMemoria;
    }
}
