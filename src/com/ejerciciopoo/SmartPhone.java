package com.ejerciciopoo;

public class SmartPhone {
    int memoriaRam;
    int almacenamiento;
    boolean gps;
    String tipoRed;

    public SmartPhone() {
    }

    public SmartPhone(int memoriaRam, int almacenamiento, boolean gps, String tipoRed) {
        this.memoriaRam = memoriaRam;
        this.almacenamiento = almacenamiento;
        this.gps = gps;
        this.tipoRed = tipoRed;
    }
}
